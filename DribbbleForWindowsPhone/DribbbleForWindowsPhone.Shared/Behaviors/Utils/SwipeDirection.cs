namespace DribbbleForWindowsPhone.Behaviors.Utils
{
    /// <summary>
    /// The directions that are supported for swipe gesture.
    /// </summary>
    public enum SwipeDirection
    {
        /// <summary>
        /// Represents the to LEFT swipe gesture.
        /// </summary>
        Left,

        /// <summary>
        /// Represents the to RIGTH swipe gesture.
        /// </summary>
        Right,

        /// <summary>
        /// Represents the to UP swipe gesture.
        /// </summary>
        Up,

        /// <summary>
        /// Represents the to DOWN swipe gesture.
        /// </summary>
        Down,

        /// <summary>
        /// Represents the to LEFT and DOWN swipe gesture.
        /// </summary>
        LeftDown,
        /// <summary>
        /// Represents the to LEFT and UP swipe gesture.
        /// </summary>
        LeftUp,
        /// <summary>
        /// Represents the to RIGHT and DOWN swipe gesture.
        /// </summary>
        RightDown,

        /// <summary>
        /// Represents the to RIGHT and UP swipe gesture.
        /// </summary>
        RightUp
    }
}
