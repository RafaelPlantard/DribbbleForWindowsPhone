namespace DribbbleForWindowsPhone.Model
{
    /// <summary>
    /// The different categories of shots to help to filter the list of them.
    /// </summary>
    enum ShotCategory
    {
        /// <summary>
        /// Represents the popular shots.
        /// </summary>
        Popular,

        /// <summary>
        /// Represents the recent shots.
        /// </summary>
        Recent,

        /// <summary>
        /// Represents the most viewed shots.
        /// </summary>
        MostViewed,

        /// <summary>
        /// Represents the most commented shtos.
        /// </summary>
        MostCommented
    }
}
